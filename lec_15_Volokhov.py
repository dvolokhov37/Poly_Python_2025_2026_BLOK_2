# Искусственный интеллект >> Машинное обучение >> Нейросети >> Глубокое обучение - градация (что во что включено)
# Машинное обучени - есть некоторый набор точек в n-мерном пр-ве, и надо подогнать этот набор точек под соответствующую
# функцию, которая отображает входные данные на выходные данные (часть называются признаками, часть целями (целевая переменная))
# Эта функция должна:
# - улавливать важные сигналы в данных,
# - игнорировать помехи,
# - хорошо рабоать на новых неизвестных данных
# Нет знания о функции, которая породила эти данные, и эту функцию стараемся примерно её построить (оценить)
# Проблемы:
# функций м.б. много, какую выбрать - не понятно..
# оценка работы на новых данных под вопросом т.к. не с чем сравнивать по большему счёту

# Как это работает?
# есть Источник данных; из этих данных формируем Данные для обучения #1, на которых хотим обучить нашу модель
# происходит процесс обучения нашей модели, на выходе появляется модель машинного обучения № 1, далее с помощью
# полученной модели № 1 и Данных для обучения #1 осуществляем Предсказание, и получаем результат, на основе которого
# корректируем модель; далее приходят вторые данные #2, мы пихаем их в нашу модель № 1 и получаем результат №2, и
# мы должны ему верить, проверить это мы особо никак не можем

# Пример: письма, которые оцениваем на спам
# из всего мн-ва писем которые существуют (генеральной совокупности), мы собираем какую-то часть из них,
# на основе части этих данных строим модель, и с помощью неё хотим оценивать реальные письма
# очень большую роль играет соотношение (распределение) тестовых и реальных данных между собой
# здесь возникает много вещей, связ. с мат.статом и теор.вером, и именно на этих вещах чаще всего построены методы
# машинного обучения; т.е. когда начинается обучение модели - просто взять данные, прогнать через систему и получить
# результат, как-то оценить его - может быть полностью безрезультатным занятием, т.к. проблема лежит в том, какие данные
# будет использовать модель в будущем, и на каких мы её обучали.. и если эти данные никак особо не бьются меж собой - модель
# НЕ даст никакого хорошего, адекватного результата

# НО, самое главное, что мы делаем - по некоторым данным пытаемся построить некоторую функцию распределения, которая вход переводит в выход
# ПО СУТИ: обучение модели - это настройка её параметров, в этом и состоит всё обучение


## Классическое обучение делится на две части:

## Обучение с учителем
# - мы знаем на определенном этапе как оценить результат (т.е. он хороший или некорректный)

## Обучение без учителя
# - не знаем результата, но можем определить разбить данные на кусочки, или понизить размерность

## Между этими методами ещё есть группа методов частичного обучения, когда существует не полный набор метрик
# т.е. часть данных оказываются не размеченными, но они, тем не менее, существуют

## Связанные с классическим обучением методы:
## Методы обучения с подкреплением
# система улучшает свои хар-ки через обратную связь, путем взаимодействия со средой
# т.е. среда, в которой работает этот метод обучения, оценивает, насколько хорошо работает метод, и его либо награждает,
# либо штрафует, если он двигается в неправильном направлении

## Ансамблевые методы - набор существующих методов, которые некоторым образом оцениваются: параллельно обрабатывает данные
